package prog.kiev;

public class Point {
    private double x;
    private double y;

    public double getX(){
        return x;
    }
    public double getY(){
        return y;
    }

    public Point(){
        super();
    };
    public Point(double x, double y){
        super();
        this.x = x;
        this.y = y;
    }
}
